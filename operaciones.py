#Variables
x = 8
y = 4

#Operaciones
print (f'Si x vale {x} e y vale {y}:')
print (f'La suma de x + y, o sea {x} + {y}, es igual a {x + y}')
print (f'La resta de x - y, o sea {x} - {y}, es igual a {x - y}')
print (f'La multiplicación de x * y, o sea {x} * {y}, es igual a {x * y}')
print (f'La división de x / y, o sea {x} * {y}, es igual a {x / y}')
print (f'El módulo de x % y, que en otras palabras sería el resto de la división entre x e y, o sea {x} % {y}, es igual a {x % y}')
